local function addDecalToPart(part)
    -- Ki·ªÉm tra xem Part c√≥ ph·∫£i l√† BasePart kh√¥ng
    if part:IsA("BasePart") then
        -- L·∫∑p qua t·∫•t c·∫£ 6 m·∫∑t c·ªßa Part
